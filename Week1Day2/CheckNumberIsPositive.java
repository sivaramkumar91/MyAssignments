public class CheckNumberIsPositive {
	public static void main(String[] args) {
		int Number = 10;
		if (Number>0) {
			System.out.println("Number is Positive");		
		}else if (Number<0) {
			System.out.println("Number is Negative");
		}else{
			System.out.println("Number is Zero");
		}
	}

}
